package skt.tmall.common.repository.temp;

import java.io.File;

import skt.tmall.common.repository.IHandle;

/**
 * Jackrabbit을 사용하려 했으나 일정상 임시로 구현해 두었다.
 * 
 * @author cshan
 * 
 */
public class TempHandle implements IHandle {

	private File file;

	public TempHandle(File file) {
		this.file = file;
	}

	public File getFile() {
		return this.file;
	}

}
